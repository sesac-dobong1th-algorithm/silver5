
#일단 무식하게 +1 하는 문제로 풀어보았는데 이걸로 해결되어서 일단 이상태로 제출.

n = int(input())
# 현재까지 찾은 '종말의 수'의 개수를 셀 변수입니다.
count = 0
# 검사를 시작할 첫 번째 숫자입니다. '종말의 수'는 666부터 시작합니다.
num = 666

# 원하는 N번째 '종말의 수'를 찾을 때까지 무한 반복합니다.
while True:
    # 현재 숫자(num)를 문자열로 변환하여 '666'이 포함되어 있는지 검사합니다.
    if '666' in str(num):
        # '666'이 포함되어 있으면, count를 1 증가시킵니다.
        count += 1
        # 만약 현재 숫자가 목표하는 N번째 '종말의 수'라면,
        if count == n:
            # 그 숫자를 출력하고,
            print(num)
            # 반복문을 종료합니다.
            break
    # 만약 현재 숫자가 '종말의 수'가 아니거나, 아직 목표에 도달하지 않았다면,
    # num을 1 증가시켜 다음 숫자로 넘어갑니다.
    num += 1